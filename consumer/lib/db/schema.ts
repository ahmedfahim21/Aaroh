export const document = pgTable(
  "Document",
  {
    id: uuid("id").notNull().defaultRandom(),
    createdAt: timestamp("createdAt").notNull(),
    title: text("title").notNull(),
    content: text("content"),
    kind: varchar("text", { enum: ["text", "code", "image", "sheet"] })
      .notNull()
      .default("text"),
    userId: uuid("userId")
      .notNull()
      .references(() => user.id),
  },
  (table) => {
    return {
      pk: primaryKey({ columns: [table.id, table.createdAt] }),
    };
  }
);

export type Document = InferSelectModel<typeof document>;

export const suggestion = pgTable(
  "Suggestion",
  {
    id: uuid("id").notNull().defaultRandom(),
    documentId: uuid("documentId").notNull(),
    documentCreatedAt: timestamp("documentCreatedAt").notNull(),
    originalText: text("originalText").notNull(),
    suggestedText: text("suggestedText").notNull(),
    description: text("description"),
    isResolved: boolean("isResolved").notNull().default(false),
    userId: uuid("userId")
      .notNull()
      .references(() => user.id),
    createdAt: timestamp("createdAt").notNull(),
  },
  (table) => ({
    pk: primaryKey({ columns: [table.id] }),
    documentRef: foreignKey({
      columns: [table.documentId, table.documentCreatedAt],
      foreignColumns: [document.id, document.createdAt],
    }),
  })
);

export type Suggestion = InferSelectModel<typeof suggestion>;

export const stream = pgTable(
  "Stream",
  {
    id: uuid("id").notNull().defaultRandom(),
    chatId: uuid("chatId").notNull(),
    createdAt: timestamp("createdAt").notNull(),
  },
  (table) => ({
    pk: primaryKey({ columns: [table.id] }),
    chatRef: foreignKey({
      columns: [table.chatId],
      foreignColumns: [chat.id],
    }),
  })
);

export type Stream = InferSelectModel<typeof stream>;

export const merchant = pgTable("Merchant", {
  id: uuid("id").primaryKey().notNull().defaultRandom(),
  slug: text("slug").notNull().unique(),
  name: text("name").notNull(),
  walletAddress: text("walletAddress").notNull(),
  categories: text("categories").notNull().default(""),
  tags: text("tags").notNull().default(""),
  description: text("description").notNull().default(""),
  createdAt: timestamp("createdAt").notNull().defaultNow(),
});

export type Merchant = InferSelectModel<typeof merchant>;

export const agent = pgTable("Agent", {
  id: uuid("id").primaryKey().notNull(),
  name: text("name").notNull(),
  instructions: text("instructions").notNull().default(""),
  walletAddress: text("walletAddress").notNull(),
  erc8004Id: text("erc8004Id"), // EIP-8004 agentId (uint256 as string), null if not registered
  createdAt: timestamp("createdAt").notNull().defaultNow(),
});

export type Agent = InferSelectModel<typeof agent>;

export const agentSession = pgTable("AgentSession", {
  id: text("id").primaryKey().notNull(),
  agentId: uuid("agentId")
    .notNull()
    .references(() => agent.id, { onDelete: "cascade" }),
  task: text("task").notNull(),
  status: varchar("status", { enum: ["running", "done", "failed"] })
    .notNull()
    .default("running"),
  result: text("result"),
  events: json("events")
    .$type<Record<string, unknown>[]>()
    .notNull()
    .default([]),
  createdAt: timestamp("createdAt").notNull().defaultNow(),
  completedAt: timestamp("completedAt"),
});

export type AgentSession = InferSelectModel<typeof agentSession>;
